from enum import Enum
from dataclasses import dataclass


class RegisterGroup(Enum):
    Accum = 0
    Code = 1
    Data = 2


@dataclass
class TapData:
    offset: int
    back: int
    group: RegisterGroup
    combo: int
    skip: int


@dataclass
class TapSet:
    taps: list[TapData]
    combo_taps: list[int]
    combo_begin: list[int]
    group_begin: list[int]
    combos_count: int
    reg_count: int
    tot_combo_backs: int


def get_register_taps() -> list[tuple[int, TapData]]:
    cursor = 0
    taps: list[tuple[int, TapData]] = []
    while cursor < len(TAPSET.taps):
        t = TAPSET.taps[cursor]
        taps.append((cursor, t))
        cursor += t.skip
    return taps


